export function calculateLevel(score: number): "low" | "medium" | "high" {
  const maxScore = 36; // 12 questions × 3 points
  const percentage = (score / maxScore) * 100;
  
  if (percentage >= 70) return "low"; // Good psychological state
  if (percentage >= 40) return "medium"; // Moderate challenges
  return "high"; // Significant challenges
}
