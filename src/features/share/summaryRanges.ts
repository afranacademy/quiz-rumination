export function pickSummaryRange(score: number): SummaryRange {
  if (score < 0 || score > 48) {
    throw new Error(`Score ${score} is out of range [0, 48]`);
  }

  for (const range of SUMMARY_RANGES) {
    if (score >= range.min && score <= range.max) {
      return range;
    }
  }

  // Fallback (should never happen)
  return SUMMARY_RANGES[SUMMARY_RANGES.length - 1];
}

